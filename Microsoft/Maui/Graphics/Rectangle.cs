namespace Microsoft.Maui.Graphics
{
    internal class Rectangle
    {
        private double v1;
        private double y;
        private int v2;
        private int v3;

        public Rectangle(double v1, double y, int v2, int v3)
        {
            this.v1 = v1;
            this.y = y;
            this.v2 = v2;
            this.v3 = v3;
        }
    }
}